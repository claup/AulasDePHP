<?php
/*
 FOR é utilizado para coisas muito bem definidas, ações do tipo de 1 a 100! LIMITE CLARO.
 WHILE é ideal para ações mais lógicas, quando preciso saber se o próximo passo deve ou não ser executado!
 LIMITE INDEFINIDO, porque vai depender da operação lógica que será escrita.
 */

for($numero=0; $numero<=100; $numero++)
{
	echo $numero. "\n";
}

?>